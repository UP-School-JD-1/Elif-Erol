package Homework8;

public class Location {
	public String depot;
	public String place;

	public Location(String depot, String place) {
		super();
		this.depot = depot;
		this.place = place;
	}

	public String getDepot() {
		return depot;
	}

	public void setDepot(String depot) {
		this.depot = depot;
	}

	public String getPlace() {
		return place;
	}

	public void setPlace(String place) {
		this.place = place;
	}

}
